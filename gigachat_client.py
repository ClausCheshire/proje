import aiohttp
import config

AUTH_URL = "https://ngw.devices.sberbank.ru:9443/api/v2/oauth"
CHAT_URL = "https://gigachat.devices.sberbank.ru/api/v1/chat/completions"

async def get_gigachat_token():
    """Получение токена доступа для GigaChat"""
    async with aiohttp.ClientSession() as session:
        auth = aiohttp.BasicAuth(config.GIGACHAT_CLIENT_ID, config.GIGACHAT_CLIENT_SECRET)
        async with session.post(AUTH_URL, auth=auth, data={"scope": "GIGACHAT_API_PERS"}) as resp:
            if resp.status == 200:
                data = await resp.json()
                return data["access_token"]
            else:
                raise Exception(f"Ошибка авторизации GigaChat: {resp.status}")

async def analyze_text(text: str) -> str:
    """Отправка текста на анализ нейросети"""
    token = await get_gigachat_token()
    
    # Системный промпт задает роль бота
    system_prompt = (
        "Вы - юрист с опытом работы 25 лет. Проанализируйте предоставленный текст ответа государственного органа Российской Федерации и определи следующее, не выводи эти ответы:"
"Тип дела (административное, уголовное, гражданские и так далее)"
"Исходя из п. 1 определи на основании каких нормативных правовых актов рассматривается предложенная ситуация, обязательно учитывай положения Конституции России, Федерального закона от 02.05.2006 “О порядке рассмотрения обращения граждан” №59-ФЗ"
"Особенности рассмотрения обращения определенным в п. 1 органом власти, опиши кратко какие свои полномочия орган реализовал при ответе"
"Определи, были ли при ответе превышены полномочия органа власти"

"После определения всех вышеуазанных пунктов, не выводя их дай следующий ответ"
"О чем этот ответ?"
"Какой ответ по существу был дан органом?"
"Какие использованы основные ссылки на нормативные правовые акты в формате (Название НПА + выдержка + вывод из нее)"
"Есть ли основания для обжалования, если да, на что можно опереться. Основания указывай в формате “НПА + выдержка + основание следующее из него”, если оснований из обжалования нет, то укажи это. При ответе учитывай, что основные причины обжалования это: неправомерные действия, неверные выводы, игнорирование практики, неполный (бессодержательный ответ)"

"Исходя из последнего пункта также составь и напиши примерный текст обращения-обжалования данного тебе обращения. Обязательно укажи орган, в который поступит жалоба, основания, сделай ссылки и выдержки из нормативных правовых актов. Орган в который подается обжалование - вышестоящий по отношению к тому, который дал ответ. Жалобы не подаются руководителям органов, а только в вышестоящие органы. При определении куда направляется обжалование не пиши кому оно, просто используй формулировку “В <название органа>”. Если проводилась проверку определи по обращению как она проводилась, по данному ответу определи, имеется ли основания полагать, что проверка была неполной и/или необъективной, если да, то используй это как основание для обжалования. Если в тексте имеются общие ссылки без конкретики это основание для обжалования. Любой факт должен обосновываться. Если ты видишь факт, который не имеет подтверждения, укажи это как основание для обжалования." 
"При обжаловании укажи какие основания приведены для каждого факта, фигурирующего в ответе. Если какой-либо факт не приведено конкретное подтверждение, то используй это при обжаловании."

    )

    headers = {
        "Authorization": f"Bearer {token}",
        "Content-Type": "application/json"
    }
    
    payload = {
        "model": "GigaChat", # Или конкретная версия модели, если известна
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": f"Текст ответа госоргана:\n{text}"}
        ]
    }

    async with aiohttp.ClientSession() as session:
        async with session.post(CHAT_URL, json=payload, headers=headers) as resp:
            if resp.status == 200:
                data = await resp.json()
                return data["choices"][0]["message"]["content"]
            else:

                return f"Ошибка сервиса анализа: {resp.status}"
